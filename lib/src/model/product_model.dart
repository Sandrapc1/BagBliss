class ProductDetails {
  final String? name;
  final String? category;
  final String? quantity;
  final String? size;
  final String? price;
  final String? description;
  final String? image;
  final String? brand;
  final String? id;

  ProductDetails(
     {
      this.id, 
    this.name,
    this.category,
    this.quantity,
    this.size,
    this.price,
    this.description,
    this.image,
    this.brand,
  });
}
